/*
================
List interface
================

The List interface is an ordered collection that allows us to store and
access elements sequentially.

The List interface of the Java collections framework provides the
functionality of the dynamic list data structure.

It extends the Collection interface.

============================
Classes that Implement List
============================

ArrayList
LinkedList
Vector
Stack

=================
Methods of List
=================

=> add() - adds an element to a list

=> addAll() - adds all elements of one list to another

=> get() - helps to randomly access elements from lists

=> iterator() - returns iterator object that can be used to sequentially
access elements of lists

=> set() - changes elements of lists

=> remove() - removes an element from the list

=> removeAll() - removes all the elements from the list

=> clear() - removes all the elements from the list (more efficient than
removeAll())

=> size() - returns the length of lists

=> toArray() - converts a list into an array

=> contains() - returns true if a list contains specific element

 */